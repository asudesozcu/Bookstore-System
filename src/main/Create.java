    private void Createbtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Createbtn1ActionPerformed
        if (jCheckBox1.isSelected() == true) {
            int day = Integer.parseInt(jSpinner1.getValue().toString());
            int month = Integer.parseInt(jSpinner2.getValue().toString());
            int year = Integer.parseInt(jSpinner3.getValue().toString());

            LocalDate birthDate = LocalDate.of(year, month, day);
            LocalDate today = LocalDate.now();
            Period period = Period.between(birthDate, today);

            int age = period.getYears();

            if (age < 18) {
                JOptionPane.showMessageDialog(this, "You must be at least 18 years old.", "Age Restriction", JOptionPane.WARNING_MESSAGE);
            } else {
                String url = "jdbc:mysql://localhost:3306/mysql?zeroDateTimeBehavior=CONVERT_TO_NULL";

                try ( Connection connection = DriverManager.getConnection(url, "root", "root")) {
                    String query = "SELECT * FROM customers WHERE username = ?";
                    try ( PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                        preparedStatement.setString(1, Username.getText());
                        try ( ResultSet resultSet = preparedStatement.executeQuery()) {

                            if (resultSet.next()) {
                                JOptionPane.showMessageDialog(this, "Username is already taken. Please choose another one.", "Username Check", JOptionPane.WARNING_MESSAGE);
                            } else {
                                Customer customer = new Customer(Username.getText(), Name.getText(), Surname.getText(), mail.getText(), phone.getText(), jTextArea1.getText(), Arrays.toString(jPasswordField2.getPassword()), Person.Gender.MALE, Integer.parseInt(ID.getText()), age);

                                if (MALE.isSelected()) {
                                    customer.setGender(Person.Gender.MALE);
                                } else if (female.isSelected()) {
                                    customer.setGender(Person.Gender.FEMALE);
                                } else {
                                    customer.setGender(Person.Gender.NOTPREFERED);
                                }

                                DataBase.customers.add(customer);

                                String insertQuery = "INSERT INTO customers (id, name, surname, username, email, phone, password, address, gender, age) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
                                try ( PreparedStatement insertStatement = connection.prepareStatement(insertQuery)) {
                                    insertStatement.setInt(1, customer.getId());
                                    insertStatement.setString(2, customer.getName());
                                    insertStatement.setString(3, customer.getSurname());
                                    insertStatement.setString(4, customer.getUsername());
                                    insertStatement.setString(5, customer.getEmail());
                                    insertStatement.setString(6, customer.getPhone());
                                    insertStatement.setString(7, customer.getPassword());
                                    insertStatement.setString(8, customer.getAddress());
                                    insertStatement.setString(9, customer.getGender().toString());
                                    insertStatement.setInt(10, age);

                                    int rowsAffected = insertStatement.executeUpdate();

                                    if (rowsAffected > 0) {
                                        System.out.println("Customer added successfully!");
                                    } else {
                                        System.out.println("Failed to add customer.");
                                    }
                                }
                            }
                        }
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error while checking username.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            CustomerLog覺n newframe = new CustomerLog覺n();
            newframe.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Please check confirm button");
        }

    }//GEN-LAST:event_Createbtn1ActionPerformed

